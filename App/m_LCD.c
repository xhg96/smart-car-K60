/******************************************************************************
*  @brief      显示一幅220*176的位图
*  @param      Str     位图字符串首地址
*  @param      Color   位图颜色
*  @param      bkColor 背景颜色
*****************************************************************************/
void mLCD_pic(Site_t site, Size_t size, const uint8 *pic, uint16 Color, uint16 bkColor)
{
    uint16 i,j;
    uint8 tmp_char=0;
    
    LCD_PTLON(site, size);              //开窗
    LCD_RAMWR();                        //写内存
    
    for (i = 0; i < size.W * size.H / 8; i++)
    {
        tmp_char = *pic++;
        
        for(j = 0; j < 8; j++)
        {
            if(tmp_char & (0x80))
            {
                LCD_WR_DATA( Color );
            }
            else
            {
                LCD_WR_DATA( bkColor );
            }
            tmp_char <<= 1;
        }
    }
}

