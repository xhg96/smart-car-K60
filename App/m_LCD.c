/******************************************************************************
*      @brief      显示字符串
*      @param      COLUMN,ROW		左上角坐标
*      @param      Str     字符串地址
*      @param      Color   字体颜色
*      @param      bkColor 背景颜色
*  Sample usage:       mLCD_str(10,20,"www.vcan123.com", BLUE,RED);
******************************************************************************/
void mLCD_str(uint8 x,uint8 y,const uint8 *Str,uint16 Color,uint16 bkColor)
{
#define MAX_CHAR_POSX (220-8)
#define MAX_CHAR_POSY (176-16)
    
	Site_t site;
	site.x = x;    site.y = y;
	
    while(*Str != '\0')
    {
        if(site.x > MAX_CHAR_POSX )
        {
            //换行
            site.x = 0;
            site.y += 16;
        }
        if(site.y > MAX_CHAR_POSY )
        {
            //一屏
            site.y = 0;
            site.x = 0;
        }
        
        LCD_char(site, *Str, Color, bkColor);
        site.x += 8;
        Str ++ ;
    }
#undef MAX_CHAR_POSX
#undef MAX_CHAR_POSY
}
/******************************************************************************
*      @brief      显示数字
*      @param      site    左上角坐标
*      @param      num     数字
*      @param      Color   字体颜色
*      @param      bkColor 背景颜色
*  Sample usage:        mLCD_num(0, 20, 2, 123, BLUE,RED);
******************************************************************************/
void mLCD_num(uint8 x, uint8 y, int num, uint8 max_num_bit, uint16 Color, uint16 bkColor)
{
	Site_t site;
	site.x = x;  site.y = y;
	
    uint32 res,res_t;
    uint8 t = 0;
    uint8 tt= 0;
    
    Site_t sitetemp;
	Size_t size;
	
    sitetemp.y = site.y;
    
    if( num == 0 )
    {
        LCD_char(site, '0', Color, bkColor);
        
        site.x += 8;
        size.H  = 16;
        size.W  = 8 * (max_num_bit - 1);
        LCD_rectangle(site, size, bkColor);
        
        return;
    }
	else if( num < 0)	//负数处理
	{
		LCD_char(site, '-', Color, bkColor);
		site.x += 8;
		res = -num;
	}
	else
	{
		res = num;
	}
	
    res_t = res;
    while( res_t )            /*得到数字长度t*/
    {
        res_t /= 10;
        t++;
    }
    if(t >= max_num_bit )    //限制最大长度
    {
        t = max_num_bit;
    }
    
    tt = t;
    
    while( t != 0 )
    {
        sitetemp.x = site.x + (8 * (t--) - 8);
        LCD_char(sitetemp, (res % 10) + '0', Color, bkColor);
        res /= 10 ;
    }
    
    if(res != max_num_bit )
    {
        size.W = 8 * (max_num_bit - tt);
        site.x += (8 * tt);
        size.H  = 16;
        LCD_rectangle(site, size, bkColor);
    }
}

